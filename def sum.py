# sum function

# Define a function named 'sum' that takes two parameters 'a' and 'b' 
def sum(a, b):
    # Calculate the sum of 'a' and 'b' and store it in the variable 'answer'
    answer = a + b
    # Return the calculated sum
    return answer

# Initialize the first number with the value 4
first_number = 4
# Initialize the second number with the value 5
second_number = 5

# Call the 'sum' function with 'first_number' and 'second_number' as arguments
# Store the result in the variable 'sum_of_2_numbers'
sum_of_2_numbers = sum(first_number, second_number)

# Print the result of the sum
print(sum_of_2_numbers)
